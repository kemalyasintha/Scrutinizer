    private void performSummation(java.awt.event.ActionEvent evt) {
        double total = 0.0;
        if (txtconfirm.getText().length() > 0) {
            try {
                double a = Double.parseDouble(txtpamount.getText());
                double b = Double.parseDouble(txtconfirm.getText());
                double c = Double.parseDouble(txtOutstanding.getText());
                if (a == b) {
                    if(a<= c){
                    total += b;
                    txtrecived.setBackground(Color.CYAN);
                    paneloustd.setVisible(true);
                    txtrecived.setText(Double.toString(total));
                    setnewoutsd();
                    }
                } else {
                    paneloustd.setVisible(false);
                    txtrecived.setBackground(Color.WHITE);
                    txtrecived.setText(Double.toString(total));
                }
            } catch (NumberFormatException nbx) {
            }
        }

        //txtrecived.setBackground(Color.CYAN);
    }

    public void setnewoutsd() {
        txtPayable1.setText(txtPayable.getText());
        Double newpaid = Double.parseDouble(txtTotalPaid.getText()) + Double.parseDouble(txtrecived.getText());
        txtTotalPaid1.setText(Double.toString(newpaid));
        Double newoutsd = Double.parseDouble(txtPayable1.getText()) - Double.parseDouble(txtTotalPaid1.getText());
        txtOutstanding1.setText(Double.toString(newoutsd));
    }

