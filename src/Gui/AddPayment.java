    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        DBOperation db = new DBOperation();
        Paymentdetails pd = new Paymentdetails();
        Tourtable tt = new Tourtable();
        try {
            Double amount = Double.parseDouble(txtpamount.getText());
            Double confirm = Double.parseDouble(txtconfirm.getText());
            //Double recieve = Double.parseDouble(txtrecived.getText());
            pd.setCustId(Integer.parseInt(txtcustid.getText()));
            pd.setTourId(Integer.parseInt(txtTourId.getText()));
            pd.setUsername(user);
            
            //System.out.println(user);
            //System.out.println(amount.compareTo(confirm));
            if (amount > 0 &&  amount.compareTo(confirm)==0) {
                pd.setAmount(confirm);
                boolean paytbl = db.setpaymenttable(pd);
                if (paytbl) {
                    boolean ttble = db.paymenttour(Integer.parseInt(txtcustid.getText()), Integer.parseInt(txtTourId.getText()), Double.parseDouble(txtTotalPaid1.getText()));
                    if (ttble) {
                        JOptionPane.showMessageDialog(this, "RS:" + confirm + "/- " + "Credited Successfully");
                        btnConfirm.setEnabled(false);
                        btnPrint.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Network error try again..!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please enter valid amount..!");
            }
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "1Please enter valid amount..!");
        }    
    }//GEN-LAST:event_btnConfirmActionPerformed

