    private void btnmoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmoreActionPerformed
        // TODO add your handling code here:
        //Customerpaymentdetails cd= cust1.get(tblpayments.getSelectedRow());
        //Userpaymentdetails ud=user1.get(tblpayments.getSelectedRow());
        try{
            if(cbcustomer.isSelected()){
            Customerpaymentdetails cd= cust1.get(tblpayments.getSelectedRow());
            Viewpaymentsmoreadmin v=new Viewpaymentsmoreadmin();
            v.setPaymentid(cd.getPaymentid());
            v.setfileds(db.viewmorepay(cd.getPaymentid()));
            v.setpayid(Integer.toString(cd.getPaymentid()));
            v.setVisible(true);
            v.setDefaultCloseOperation(HIDE_ON_CLOSE);
        }
        else if(cbtour.isSelected()){
            Tourpaymentdetails tp=tour1.get(tblpayments.getSelectedRow());
            Viewpaymentsmoreadmin v=new Viewpaymentsmoreadmin();
            v.setfileds(db.viewmorepay(tp.getPaymentid()));
            v.setPaymentid(tp.getPaymentid());
            v.setpayid(Integer.toString(tp.getPaymentid()));
            v.setVisible(true);
            v.setDefaultCloseOperation(HIDE_ON_CLOSE);
        }
        else{
            Userpaymentdetails ud=user1.get(tblpayments.getSelectedRow());
            Viewpaymentsmoreadmin v=new Viewpaymentsmoreadmin();
            v.setfileds(db.viewmorepay(ud.getPaymentId()));
            v.setpayid(Integer.toString(ud.getPaymentId()));
            v.setPaymentid(ud.getPaymentId());
            v.setVisible(true);
            v.setDefaultCloseOperation(HIDE_ON_CLOSE);
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Please Select a tour !!!!  ");
        }
        

    }//GEN-LAST:event_btnmoreActionPerformed

    private void btnviewtableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnviewtableActionPerformed
        // TODO add your handling code here:
        if (cbcustomer.isSelected() | cbtour.isSelected() | cbuser.isSelected()) {
            if (cbcustomer.isSelected() & cbthisday.isSelected()) {
                loadcustomertable(currentdate);
            } else if (cbcustomer.isSelected() & cbthismonth.isSelected()) {
                loadcustatblethistearthismonth(year, month);
            } else if (cbcustomer.isSelected() & cbthisweek.isSelected()) {
                loadcustatblethistearthisweek(year, week);

            } else if (cbcustomer.isSelected() & cbthisyear.isSelected()) {
                loadcustatblethistear(year);

            } else if (cbtour.isSelected() & cbthisday.isSelected()) {
                loadtourtable(currentdate);

            } else if (cbtour.isSelected() & cbthismonth.isSelected()) {
                loadtourtablebyyearthismonth(year, month);

            } else if (cbtour.isSelected() & cbthisweek.isSelected()) {
                loadtourtablebyyearthisweek(year, week);

            } else if (cbtour.isSelected() & cbthisyear.isSelected()) {
                loadtourtablebyyear(year);

            } else if (cbuser.isSelected() & cbthisday.isSelected()) {
                loadusertable(currentdate);

            } else if (cbuser.isSelected() & cbthismonth.isSelected()) {
                loadusertablethisyearthismonth(year, month);

            } else if (cbuser.isSelected() & cbthisweek.isSelected()) {
                loadusertablethisyearthisweek(year, week);

            } else if (cbuser.isSelected() & cbthisyear.isSelected()) {
                loadusertablethisyear(year);

            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Please selsct a view category!!!");
        }
    }//GEN-LAST:event_btnviewtableActionPerformed

