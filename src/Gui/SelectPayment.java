    void loadtable(ArrayList<Integer> regtlist) {
        tlist = db.getregtourlist(regtlist);
            
         if (tlist.isEmpty()) {
            //System.out.println(regtlist.isEmpty());
            //System.out.println(regtlist.size());
            txtpending.setText("No Pending Tours Available for this Customer..! ");
           
            
        } else {
            //tlist = db.getregtourlist(regtlist);
            ToursList tl = new ToursList(tlist);
            tblupdatetour.setModel(tl);
        }

    }

