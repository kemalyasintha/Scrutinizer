    private void btnaddpaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddpaymentActionPerformed
        // TODO add your handling code here:
      try{
        tourdetails td = tlist.get(tblupdatetour.getSelectedRow());
        AddPayment adp=new AddPayment();
        adp.setpersonaldetails(csd);
        adp.setUser(cuser);
        Tourtable tt=db.viewtourtable(td.getTourid(), csd.getCustid());
        adp.setprices(tt);
        adp.settourdetails(td);
        adp.setDefaultCloseOperation(HIDE_ON_CLOSE);
        this.dispose();
        //System.out.println(tlist.get(tblTours.getSelectedRow()));
        adp.setVisible(true);
        }catch(Exception e){
        JOptionPane.showMessageDialog(this, " Please select a tour...! ");
        }
    }//GEN-LAST:event_btnaddpaymentActionPerformed

