    public boolean setdiscount(int custid, int tourid) {
        tourdetails td = db.viewTourDetails(tourid);
        int count = db.noOfTours(custid);
        double margin = td.getProfitmargin();
        txtprice.setText(Integer.toString(td.getPricepercustomer()));
        txtdmax.setText(Double.toString(margin));
        if (count == 0) {
            txttourcount.setText(Integer.toString(count));
            txtDeligible.setText("00");
            return true;
        } /*else {
         return false;
         }*/

        if (td.getSecondtimepercentage() > 0 && td.getThirdtimepercentage() > 0 && td.getFourthabovepercentage() > 0 && count >= 0) {
            ddavailability.setSelectedItem("YES");
            //txttourcount.setText(Integer.toString(count));
            if (count == 1) {
                txtDeligible.setText(Integer.toString(td.getSecondtimepercentage()));
            } else if (count == 2) {
                txtDeligible.setText(Integer.toString(td.getThirdtimepercentage()));
            } else if (count >= 3) {
                txtDeligible.setText(Integer.toString(td.getFourthabovepercentage()));
            }

        } else {
            ddavailability.setSelectedItem("NO");
        }
        return true;
    }
