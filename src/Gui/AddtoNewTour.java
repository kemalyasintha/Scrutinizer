    private void btnnextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnextActionPerformed
        // TODO add your handling code here:
        String[] tour=ddtours.getSelectedItem().toString().split(" ");
        int chk = db.checkforsametour(Integer.parseInt(txtcustid.getText()), Integer.parseInt(tour[0]));
        boolean ttb = db.tourtable(Integer.parseInt(tour[0]));
        System.out.println(ttb);
       // System.out.println(tour[0]);
        switch (chk) {
            case -1: {
                JOptionPane.showMessageDialog(this, "Network error, Try Again..!");
                break;
            }
            case 0: {
                JOptionPane.showMessageDialog(this, "Already a participent of" + ddtours.getSelectedItem().toString().substring(tour[0].length()));//+ ddtours.getSelectedItem().toString().substring(4)
                break;
            }
            case 1: {
                boolean ttb1 = db.tourtable(Integer.parseInt(tour[0]));
                System.out.println(ttb);
                tourdetails tds = db.viewTourDetails(Integer.parseInt(tour[0]));
                int no = db.noOfTours(Integer.parseInt(txtcustid.getText()));
                boolean setmap = db.setmaptour(Integer.parseInt(txtcustid.getText()), Integer.parseInt(tour[0]));

                if (tds != null && no >= 0 && setmap) {
                    txtdcustid.setText(txtcustid.getText());
                    txtdtourid.setText(Integer.toString(tds.getTourid()));
                    txttourcount.setText(Integer.toString(no));
                    txtprice.setText(Integer.toString(tds.getPricepercustomer()));
                    txtdmax.setText(Double.toString(tds.getProfitmargin()));
                    if (tds.getSecondtimepercentage() > 0 && tds.getThirdtimepercentage() > 0 && tds.getFourthabovepercentage() > 0 && no >= 0) {
                        ddavailability.setSelectedItem("YES");
                        ddavailability.setEnabled(false);
                        if (no == 1) {
                            txtDeligible.setText(Integer.toString(tds.getSecondtimepercentage()));
                        } else if (no == 2) {
                            txtDeligible.setText(Integer.toString(tds.getThirdtimepercentage()));
                        } else if (no >= 3) {
                            txtDeligible.setText(Integer.toString(tds.getFourthabovepercentage()));
                        }

                    } else {
                        ddavailability.setSelectedItem("NO");
                        txtDeligible.setText("00");
                        ddavailability.setEnabled(false);
                    }
                    tab.setEnabledAt(1, true);
                    tab.setSelectedIndex(1);
                } else {
                    JOptionPane.showMessageDialog(this, "Something went wrong, Try Again..!");
                }
            }
        }
    }//GEN-LAST:event_btnnextActionPerformed

    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        // TODO add your handling code here:
        if (txtamount.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please set final amount...!");
        } else {
            Tourtable tt = new Tourtable();
            //int fm = (int) Double.parseDouble(txtamount.getText());
            tt.setDiscount(Integer.parseInt(txtfinal.getText()));
            tt.setFinalamount(Double.parseDouble(txtamount.getText()));
            tt.setTourid(Integer.parseInt(txtdtourid.getText()));
            tt.setCustid(Integer.parseInt(txtdcustid.getText()));
            //boolean fsettt = db.famounttott(Integer.parseInt(txtdtourid.getText()), Integer.parseInt(txtdcustid.getText()), tt);
            boolean addtt=db.addtourtable(tt, Integer.parseInt(txtdtourid.getText()), Integer.parseInt(txtdcustid.getText()));
            if (addtt) {
                String[] tour=ddtours.getSelectedItem().toString().split(" ");
                JOptionPane.showMessageDialog(this, "Customer added to tour " + tour[1]);
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnsubmitActionPerformed
    public void setfamount() {
        int finald = -1;
        try {
            finald = Integer.parseInt(txtfinal.getText());
            //System.out.println("An integer");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Enter valid percentage...!");
        }
        if (finald >= 0 && finald <= Double.parseDouble(txtdmax.getText())) {
            Double price = Double.parseDouble(txtprice.getText());
            Double famount = price - (price * (finald) / 100);
            txtamount.setText(Double.toString(famount));
        } else {
            txtfinal.setBackground(Color.YELLOW);
        }

    }

